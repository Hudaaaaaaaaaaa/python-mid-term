opr=str(input("enter any operator"))
number1=(int(input("Enter any number1 ")))
number2=(int(input("Enter any number2 ")))
if opr == "+":
    print(number1 + number2)
elif opr== "-":
    print(number1 - number2)
elif opr == "*":
    print(number1 * number2)
elif opr=="/":
    print(number1 / number2)
else:
    print("Invalid operator")

